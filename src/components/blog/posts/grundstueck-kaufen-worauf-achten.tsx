      <li>Liegt dagegen ein <strong>einheitliches Vertragswerk</strong> vor, bei dem Grundstück und Haus rechtlich verknüpft sind, kann die Steuer auf den Gesamtpreis aus Grundstück und Gebäude erhoben werden.</li>

      <p>Fragen Sie konkret: Ist der ursprüngliche Wechselrichter noch verbaut oder wurde er bereits getauscht? Ein neuer Wechselrichter ist ein echter Pluspunkt; ein alter ein kalkulierbares Risiko.</p>
      

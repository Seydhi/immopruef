      <p>Ein Forward-Darlehen ist im Kern eine Wette gegen den Markt. Steigen die Zinsen bis zum Start tatsächlich stärker als der Aufschlag, haben Sie gewonnen. Bleiben sie gleich oder fallen sie, hätten Sie ohne Forward-Darlehen günstiger finanziert – und der Aufschlag war umsonst. Niemand kann die Zinsentwicklung sicher vorhersagen, deshalb sollten Sie ein Forward-Darlehen als Absicherung verstehen, nicht als Spekulation auf Gewinn.</p>
      
